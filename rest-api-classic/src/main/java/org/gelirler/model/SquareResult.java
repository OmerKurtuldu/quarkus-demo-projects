package org.gelirler.model;

public class SquareResult {
    private int number;
    private int square;

    public SquareResult(int number, int square) {
        this.number = number;
        this.square = square;
    }

    public int getNumber() {
        return number;
    }

    public int getSquare() {
        return square;
    }
} 